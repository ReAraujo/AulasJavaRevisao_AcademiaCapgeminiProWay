package modulo1;

public class a013 {
    public static void main(String[] args) {

        for(int linha = 1; linha <= 10; linha ++){
            System.out.printf("%d ",linha);

            for(int coluna = 1; coluna <= 10; coluna ++){
                System.out.printf("%d ",coluna);
            }
            System.out.println();
        }

    }
    
}
